package aula27do3;

public class Medicos {
	private String nome;
	private int crm;
	private String especialidade;
	
	public String getNome() 
	{
		return nome;
	}
	
	public void setNome(String x) 
	{
		nome = x;
	}
	
	public int getCrm() 
	{
		return crm;
	}
	
	public void setCrm(int x) 
	{
		crm = x;
	}
	
	public String getEspecialidade() 
	{
		return especialidade;
	}
	
	public void setEspecialidade(String x) 
	{
		especialidade = x;
	}
	
	

}
